package com.wzitech.gamegold.common.tradeorder.client;

import java.util.List;

import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * @author SunChengfei
 *
 */
public class GeneBizOffer {
	private List<BizOffer> bizOfferList;
	
	private int pageIndex;
	
	private int pageSize;
	
	private int totalPage;
	
	private int totalRecords;

	/**
	 * @return the bizOfferList
	 */
	public List<BizOffer> getBizOfferList() {
		return bizOfferList;
	}

	/**
	 * @param bizOfferList the bizOfferList to set
	 */
	@JsonProperty("BizOffers")
	public void setBizOfferList(List<BizOffer> bizOfferList) {
		this.bizOfferList = bizOfferList;
	}

	/**
	 * @return the pageIndex
	 */
	public int getPageIndex() {
		return pageIndex;
	}

	/**
	 * @param pageIndex the pageIndex to set
	 */
	@JsonProperty("PageIndex")
	public void setPageIndex(int pageIndex) {
		this.pageIndex = pageIndex;
	}

	/**
	 * @return the pageSize
	 */
	public int getPageSize() {
		return pageSize;
	}

	/**
	 * @param pageSize the pageSize to set
	 */
	@JsonProperty("PageSize")
	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}

	/**
	 * @return the totalPage
	 */
	public int getTotalPage() {
		return totalPage;
	}

	/**
	 * @param totalPage the totalPage to set
	 */
	@JsonProperty("TotalPage")
	public void setTotalPage(int totalPage) {
		this.totalPage = totalPage;
	}

	/**
	 * @return the totalRecords
	 */
	public int getTotalRecords() {
		return totalRecords;
	}

	/**
	 * @param totalRecords the totalRecords to set
	 */
	@JsonProperty("TotalRecords")
	public void setTotalRecords(int totalRecords) {
		this.totalRecords = totalRecords;
	}
	
}
