package com.wzitech.gamegold.common.enums;

/**
 * 退款审核状态
 */
public enum RefundStatus {
    /**
     * 待审核
     */
    UnAudited(1),

    /**
     * 审核不通过
     */
    UnPassAudited(2),

    /**
     * 已退款
     */
    Refund(3);

    /**
     * 类型码
     */
    private int code;

    RefundStatus(int code){
        this.code = code;
    }

    /**
     * @return the code
     */
    public int getCode() {
        return code;
    }
}
