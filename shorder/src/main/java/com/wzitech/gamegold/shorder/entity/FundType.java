package com.wzitech.gamegold.shorder.entity;

/**
 * 收货资金类型
 */
public enum FundType {
    RECHARGE(1, "充值"),
    PAYMENT(2, "付款"),
    REFUND(3, "退款"),
    FREEZE(4, "冻结资金"),
    RELEASE_FREEZE(5, "解冻资金"),
    TRANSFER(6, "转账"),
    RECHARGE7Bao(7, "7Bao手动充值单"),
    RECHARGE_AUTO_7BAO(8, "7Bao自动充值单"),
    Gold_To_7BAO(9, "金币资金转7bao"),
    ;

    private int code;
    private String name;

    FundType(int code, String name) {
        this.code = code;
        this.name = name;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
