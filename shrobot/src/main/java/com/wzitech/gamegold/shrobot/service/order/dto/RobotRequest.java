package com.wzitech.gamegold.shrobot.service.order.dto;

import com.wzitech.chaos.framework.server.common.AbstractServiceRequest;

/**
 * 自动化异常转人工
 * Created by 340032 on 2017/12/29.
 */
public class RobotRequest extends AbstractServiceRequest {

    /**
     * 主订单号
     */
    private String orderId;

    /**
     * 子订单号
     */
    private Long id;


    /**
     * 异常原因
     */
    private String otherReason;

    /**
     * 撤销原因
     */
    private String reason;

    /**
     * 图片
     */
    private String imgSrc;


    /**
     * 签名
     */
    private String sign;

    /**
     * 每页笔数
     */
    private Integer pageSize;

    /**
     * 实际收货数量
     */
    private Long realCount;

    /**
     * 出货角色名
     */
    private String sellerRoleName;

    /**
     * 收货角色名
     * @return
     */
    private String gameRole;
    /**
     * 交易方式
     */
    private Integer tradeLogo;

    /**
     * 拍卖截图
     * @return
     */
    private String auctionImg;

    /**
     *是否展示给用户
     */
    private int type;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getAuctionImg() {
        return auctionImg;
    }

    public void setAuctionImg(String auctionImg) {
        this.auctionImg = auctionImg;
    }

    public Integer getTradeLogo() {
        return tradeLogo;
    }

    public void setTradeLogo(Integer tradeLogo) {
        this.tradeLogo = tradeLogo;
    }

    public String getGameRole() {
        return gameRole;
    }

    public void setGameRole(String gameRole) {
        this.gameRole = gameRole;
    }

    public Long getRealCount() {
        return realCount;
    }

    public void setRealCount(Long realCount) {
        this.realCount = realCount;
    }

    public String getSellerRoleName() {
        return sellerRoleName;
    }

    public void setSellerRoleName(String sellerRoleName) {
        this.sellerRoleName = sellerRoleName;
    }

    public Integer getPageSize() {
        return pageSize;
    }

    public void setPageSize(Integer pageSize) {
        this.pageSize = pageSize;
    }

    public String getImgSrc() {
        return imgSrc;
    }

    public void setImgSrc(String imgSrc) {
        this.imgSrc = imgSrc;
    }

    public String getSign() {
        return sign;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getOtherReason() {
        return otherReason;
    }

    public void setOtherReason(String otherReason) {
        this.otherReason = otherReason;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }
}
