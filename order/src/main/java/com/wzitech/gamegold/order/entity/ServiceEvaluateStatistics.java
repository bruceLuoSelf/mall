package com.wzitech.gamegold.order.entity;

import com.wzitech.chaos.framework.server.dataaccess.BaseEntity;

import java.util.Date;

/**
 * 客服评价统计
 * @author yemq
 */
public class ServiceEvaluateStatistics extends BaseEntity {
    /**
     * 客服账号
     */
    private String account;
    /**
     * 评价笔数
     */
    private Long totalCount;
    /**
     * 好评率
     */
    private Double goodRate;
    /**
     * 非常满意笔数
     */
    private Long veryGoodCount;
    /**
     * 满意笔数
     */
    private Long goodCount;
    /**
     * 一般笔数
     */
    private Long normalCount;
    /**
     * 不满意笔数
     */
    private Long yawpCount;
    /**
     * 非常不满意笔数
     */
    private Long veryYawpCount;
    /**
     * 默认评价笔数
     */
    private Long defaultCount;

    /**
     * 开始时间
     */
    private Date startTime;

    /**
     * 结束时间
     */
    private Date endTime;

    public Long getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(Long totalCount) {
        this.totalCount = totalCount;
    }

    public Double getGoodRate() {
        return goodRate;
    }

    public void setGoodRate(Double goodRate) {
        this.goodRate = goodRate;
    }

    public Long getVeryGoodCount() {
        return veryGoodCount;
    }

    public void setVeryGoodCount(Long veryGoodCount) {
        this.veryGoodCount = veryGoodCount;
    }

    public Long getGoodCount() {
        return goodCount;
    }

    public void setGoodCount(Long goodCount) {
        this.goodCount = goodCount;
    }

    public Long getNormalCount() {
        return normalCount;
    }

    public void setNormalCount(Long normalCount) {
        this.normalCount = normalCount;
    }

    public Long getYawpCount() {
        return yawpCount;
    }

    public void setYawpCount(Long yawpCount) {
        this.yawpCount = yawpCount;
    }

    public Long getVeryYawpCount() {
        return veryYawpCount;
    }

    public void setVeryYawpCount(Long veryYawpCount) {
        this.veryYawpCount = veryYawpCount;
    }

    public Long getDefaultCount() {
        return defaultCount;
    }

    public void setDefaultCount(Long defaultCount) {
        this.defaultCount = defaultCount;
    }

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }
}
