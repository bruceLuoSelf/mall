package com.wzitech.gamegold.order.entity;

import com.wzitech.chaos.framework.server.dataaccess.BaseEntity;

import java.util.Date;

/**
 * 客服评价记录
 * @author yemq
 */
public class ServiceEvaluate extends BaseEntity {
    /**
     * 评价的订单号
     */
    private String orderId;
    /**
     * 评价的客服ID
     */
    private Long serviceId;
    /**
     * 评分
     */
    private int score;
    /**
     * 评价内容
     */
    private String remark;
    /**
     * 评价时的订单状态
     */
    private Integer orderState;
    /**
     * 是否默认评价
     */
    private Boolean isDefault;
    /**
     * 评价者账号
     */
    private String evaluatorAccount;
    /**
     * 评价时间
     */
    private Date createTime;

    /**
     * 开始时间
     */
    private Date startTime;

    /**
     * 结束时间
     */
    private Date endTime;

    private String loginAccount;

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public Long getServiceId() {
        return serviceId;
    }

    public void setServiceId(Long serviceId) {
        this.serviceId = serviceId;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getEvaluatorAccount() {
        return evaluatorAccount;
    }

    public void setEvaluatorAccount(String evaluatorAccount) {
        this.evaluatorAccount = evaluatorAccount;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Boolean getIsDefault() {
        return isDefault;
    }

    public void setIsDefault(Boolean isDefault) {
        this.isDefault = isDefault;
    }

    public Integer getOrderState() {
        return orderState;
    }

    public void setOrderState(Integer orderState) {
        this.orderState = orderState;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

    public String getLoginAccount() {
        return loginAccount;
    }

    public void setLoginAccount(String loginAccount) {
        this.loginAccount = loginAccount;
    }
}
