package com.wzitech.gamegold.facade.frontend.service.repository.dto;

import com.wzitech.chaos.framework.server.common.AbstractServiceResponse;
import com.wzitech.chaos.framework.server.common.ResponseStatus;
import com.wzitech.gamegold.repository.entity.SellerRepository;

import java.util.List;

public class SellerRepositoryReponse extends AbstractServiceResponse {
    private Long totalCount;

    private List<SellerRepository> sellerRepositoryList;

    private ResponseStatus responseStatus;

    public ResponseStatus getResponseStatus() {
        return responseStatus;
    }

    public void setResponseStatus(ResponseStatus responseStatus) {
        this.responseStatus = responseStatus;
    }

    public List<SellerRepository> getSellerRepositoryList() {
        return sellerRepositoryList;
    }

    public void setSellerRepositoryList(List<SellerRepository> sellerRepositoryList) {
        this.sellerRepositoryList = sellerRepositoryList;
    }

    public Long getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(Long totalCount) {
        this.totalCount = totalCount;
    }
}
